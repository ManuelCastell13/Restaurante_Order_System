    private void accionTablaProducto() {
        tablaProducto.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                PreparedStatement ps = null;
                ResultSet rs = null;

                try {
                    Conexion co = new Conexion();
                    Connection conexion = co.getConnection();

                    int fila = tablaProducto.getSelectedRow();
                    String codigo = tablaProducto.getValueAt(fila, 0).toString();

                    ps = conexion.prepareStatement("select idPlatillo, nombre, precio from Platillos where idPlatillo=?");
                    ps.setString(1, codigo);
                    rs = ps.executeQuery();

                    while (rs.next()) {
                        cajaCodigo.setText(rs.getString("idPlatillo"));
                        cajaNombre.setText(rs.getString("nombre"));
                        cajaPrecio.setText(String.valueOf(rs.getDouble("precio")));
                    }

                } catch (Exception e1) {
                    System.err.println(e1);
                }
            }
        });
    }

