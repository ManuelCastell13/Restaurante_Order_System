    private void botonCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarActionPerformed
        DefaultTableModel modeloTabla = new DefaultTableModel();
        tablaProducto.setModel(modeloTabla);

        String campo = cajaBuscar.getText();
        String where = "";

        if (!"".equals(campo)) {//Si el campo no esta vacio
            where = "where codigo=" + campo;
        }

        PreparedStatement ps = null;
        ResultSet rs = null;

        try {
            Conexion co = new Conexion();
            Connection conexion = co.getConnection();

            ps = conexion.prepareStatement("select idPlatillo, nombre, precio from Platillos " + where);
            rs = ps.executeQuery();

            modeloTabla.addColumn("ID platillo");
            modeloTabla.addColumn("Nombre");
            modeloTabla.addColumn("Precio");

            //Obtener la cantidad de columnas en mysql
            ResultSetMetaData rsMD = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMD.getColumnCount();

            //Definir el ancho de mis columnas
            int anchos[] = {50, 150, 50};

            for (int i = 0; i < cantidadColumnas; i++) {
                tablaProducto.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }

            while (rs.next()) {
                Object fila[] = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i] = rs.getObject(i + 1);
                }

                modeloTabla.addRow(fila);
            }

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_botonCargarActionPerformed

    private void botonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarActionPerformed
        PreparedStatement ps = null;

        try {
            Conexion co = new Conexion();
            Connection conexion = co.getConnection();

            ps = conexion.prepareStatement("insert into Platillos (idPlatillo, nombre, precio) values (?,?,?)");
            ps.setString(1, cajaCodigo.getText());
            ps.setString(2, cajaNombre.getText());
            ps.setDouble(3, Double.parseDouble(cajaPrecio.getText()));
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Registro insertado correctamente");

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_botonInsertarActionPerformed

